
public class Sentence {
	String sentenceLowerCase;
	char firstLetter;
	
	public String getSentenceLowerCase() {
		return sentenceLowerCase;
	}
	public void setSentenceLowerCase(String sentenceLowerCase) {
		this.sentenceLowerCase = sentenceLowerCase;
	}
	public char getFirstLetter() {
		return firstLetter;
	}
	public void setFirstLetter(char firstLetter) {
		this.firstLetter = firstLetter;
	}
	
}
